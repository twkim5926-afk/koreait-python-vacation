냉장고 = ["사과", "바나나", "포도", "수박"]

# 요소 변경
냉장고[0] = "토마토"
print(냉장고)

# 요소 추가
냉장고.append("사과") # 가장 마지막 자리에 추가
냉장고.insert(0, "망고") # index 지정하여 추가

# 요소 제거
# pop(index) - index에 있는 데이터 꺼내온다
# pop() - index생략시 가장 뒷자리 데이터 꺼내온다
망고 = 냉장고.pop(0)
print(망고)
del 냉장고[0] # index 0번 삭제
냉장고.remove("사과") # 해당 값 삭제

